  const MAP = new Map();
  for (const ent of WW.TRACKER_ENTITIES) {
    for (const d of ent.dom) MAP.set(d, ent);
  }
  const BY_ID = new Map(WW.TRACKER_ENTITIES.map((e) => [e.id, e]));

  // host (+ optional Pfad) → Entity oder null
  WW.classifyHost = function classifyHost(host, path) {
    if (!host) return null;
    if (path) {
      for (const h of WW.PATH_HINTS) {
        if (h.hostRe.test(host) && h.pathRe.test(path)) return BY_ID.get(h.entityId) || null;
      }
    }
    const labels = host.split('.');
    for (let i = 0; i <= labels.length - 2; i++) {
      const ent = MAP.get(labels.slice(i).join('.'));
      if (ent) return ent;
    }
    return null;
  };

